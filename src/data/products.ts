export const getProductBySlug = (slug: string): Product | undefined =>
  products.find((p) => p.slug === slug);

export const getProductById = (id: string): Product | undefined =>
  products.find((p) => p.id === id);

export const getProductsByWorld = (worldSlug: string): Product[] =>
  products.filter((p) => p.world === worldSlug);

export const getProductsByCategory = (category: string): Product[] =>
  category === 'all' ? products : products.filter((p) => p.category === category);

export const getProductsByCollection = (collection: string): Product[] =>
  products.filter((p) => p.collection.includes(collection));

export const getFeaturedProducts = (limit = 8): Product[] =>
  products.filter((p) => p.bestSeller || p.badges.includes('bestseller')).slice(0, limit);

export const getGiftableProducts = (): Product[] =>
  products.filter((p) => p.giftable);

export const getNewInProducts = (): Product[] =>
  products.filter((p) => p.badges.includes('new') || p.collection.includes('new-in'));

export const getRelatedProducts = (productSlug: string, limit = 4): Product[] => {
  const product = getProductBySlug(productSlug);
  if (!product) return products.slice(0, limit);

  // First: same world, then: related slugs, then: same category
  const fromRelated = (product.relatedSlugs || [])
    .map((s) => getProductBySlug(s))
    .filter(Boolean) as Product[];

  const fromWorld = products.filter(
    (p) => p.slug !== productSlug && p.world === product.world && !fromRelated.find((r) => r.slug === p.slug)
  );

  return [...fromRelated, ...fromWorld].slice(0, limit);
};

